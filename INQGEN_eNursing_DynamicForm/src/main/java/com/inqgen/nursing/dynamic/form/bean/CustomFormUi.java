package com.inqgen.nursing.dynamic.form.bean;

public class CustomFormUi {
    String formType;
    String listForm;
    String addFormPrompt;
    String updateFormPrompt;
    String printFormPrompt;
    String printForm;

    public String getListForm() {
        return listForm;
    }

    public void setListForm(String listForm) {
        this.listForm = listForm;
    }

    public String getAddFormPrompt() {
        return addFormPrompt;
    }

    public void setAddFormPrompt(String addFormPrompt) {
        this.addFormPrompt = addFormPrompt;
    }

    public String getUpdateFormPrompt() {
        return updateFormPrompt;
    }

    public void setUpdateFormPrompt(String updateFormPrompt) {
        this.updateFormPrompt = updateFormPrompt;
    }

    public String getPrintFormPrompt() {
        return printFormPrompt;
    }

    public void setPrintFormPrompt(String printFormPrompt) {
        this.printFormPrompt = printFormPrompt;
    }

    public String getPrintForm() {
        return printForm;
    }

    public void setPrintForm(String printForm) {
        this.printForm = printForm;
    }

    public String getFormType() {
        return formType;
    }

    public void setFormType(String formType) {
        this.formType = formType;
    }
}
